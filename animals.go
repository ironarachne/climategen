func (climate Climate) getFilteredAnimals() []Animal {
	animals := getAllAnimals()
	animals = filterAnimalsForHumidity(climate.Humidity, animals)
	animals = filterAnimalsForTemperature(climate.Temperature, animals)
	animals = climate.filterAnimalsForWater(animals)

	return animals
}

func getRandomAnimals(amount int, from []Animal) []Animal {
	var animal Animal

	animals := []Animal{}

	if amount > len(from) {
		amount = len(from)
	}

	for i := 0; i < amount; i++ {
		animal = from[rand.Intn(len(from)-1)]
		if !isAnimalInSlice(animal, animals) {
			animals = append(animals, animal)
		}
	}

	return animals
}

func isAnimalInSlice(animal Animal, animals []Animal) bool {
	isIt := false
	for _, a := range animals {
		if a.Name == animal.Name {
			isIt = true
		}
	}

	return isIt
}

func filterAnimalsForHumidity(humidity int, animals []Animal) []Animal {
	var filteredAnimals []Animal

	for _, a := range animals {
		if a.MinHumidity <= humidity && a.MaxHumidity >= humidity {
			filteredAnimals = append(filteredAnimals, a)
		}
	}

	return filteredAnimals
}

func filterAnimalsForTemperature(temperature int, animals []Animal) []Animal {
	var filteredAnimals []Animal

	for _, a := range animals {
		if a.MinTemperature <= temperature && a.MaxTemperature >= temperature {
			filteredAnimals = append(filteredAnimals, a)
		}
	}

	return filteredAnimals
}

func (climate Climate) filterAnimalsForWater(animals []Animal) []Animal {
	var filteredAnimals []Animal

	for _, a := range animals {
		if a.IsAquatic && (climate.HasLakes || climate.HasOcean || climate.HasRivers || climate.HasWetlands) {
			filteredAnimals = append(filteredAnimals, a)
		} else if !a.IsAquatic {
			filteredAnimals = append(filteredAnimals, a)
		}
	}

	return filteredAnimals
}

